package com.learn;

public class CalcHelper {

    public static double addValues(double nr1, double nr2) {
        return nr1 + nr2;
    }

    public static double substractValues(double nr1, double nr2) {
        return nr1 - nr2;
    }

    public static double multipleValues(double nr1, double nr2) {
        return nr1 * nr2;
    }

    public static double divideValues(double nr1, double nr2) {
        return nr1 / nr2;
    }

}
